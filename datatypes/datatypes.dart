void main() {
  // Data Types

  // Numbers, Strings, Booleans, Lists (Arrays), Maps (Objects), Dynamic

  /*##### Strings #####*/

  String name = 'Anoop';

  print("String : $name");

  /*###### Integers ####*/

  int value = 7;

  print("Integer : $value");

  /*#### Double ###*/

  double value2 = 99.99;

  print("Double : $value2");

  /*#### Boolean ####*/

  bool isActive = false;

  print("Boolean : $isActive");

  /*#### Dynamic ####*/

  dynamic fullName = "anoop reddy";

  print("Dynamic : $fullName");
}
